// ===== 查询工具 =====

/** 根据slug获取模块知识库 */
export function getKnowledge(slug: string): ModuleKnowledge | undefined {
  return KNOWLEDGE_BASE.find(k => k.slug === slug);
}

/** 在指定模块的FAQ中搜索匹配的答案 */
export function searchFAQ(slug: string, query: string): FAQ | undefined {
  const knowledge = getKnowledge(slug);
  if (!knowledge) return undefined;

  const q = query.toLowerCase();
  // 1. 关键词精确匹配
  for (const faq of knowledge.faqs) {
    if (faq.keywords.some(kw => q.includes(kw))) return faq;
  }
  // 2. 问题文本模糊匹配
  for (const faq of knowledge.faqs) {
    const faqQ = faq.q.toLowerCase();
    if (q.includes(faqQ) || faqQ.includes(q)) return faq;
  }
  return undefined;
}

/** 全局搜索所有模块的FAQ */
export function searchAllFAQ(query: string): { slug: string; faq: FAQ } | undefined {
  const q = query.toLowerCase();
  for (const knowledge of KNOWLEDGE_BASE) {
    for (const faq of knowledge.faqs) {
      if (faq.keywords.some(kw => q.includes(kw))) {
        return { slug: knowledge.slug, faq };
      }
    }
  }
  return undefined;
}